#include<iostream>
using namespace std;
int main()
{
	int n;
	cout<<"Enter the No:";
	cin>>n;
	switch(n)
	{
		case 1:cout<<"ONE";
		break;
		case 2:cout<<"TWO";
		break;
		case 3:cout<<"THREE";
		break;
		case 4:cout<<"FOUR";
		break;
		case 5:cout<<"FIVE";
		break;
		default:cout<<"WRONG NUMBER";
	}
}
